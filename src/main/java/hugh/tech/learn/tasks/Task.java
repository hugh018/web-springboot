    /**
     * 使用Future以及定义超时
     *
     * @return http://blog.didispace.com/springbootasync-4/
     * @throws Exception
     */
    @Async("taskExecutor")
    public Future<String> run() throws Exception {
        long sleep = random.nextInt(10000);
        System.out.println("开始任务，需耗时：" + sleep + "毫秒");
        Thread.sleep(sleep);
        System.out.println("完成任务");
        return new AsyncResult<>("test-Future执行返回结果");
    }
